# 큰 따옴표로 문장을 열었을 때 작은 따옴표로 강조 해준다.
print("안녕하세요. '최재훈'입니다.")
# 작은 따옴표로 문장을 열었을 때 큰 따옴표로 강조 해준다.
print('안녕하세요. "최재훈"입니다')

# ''로 강조하여 교수님 '수업'마쳐주세요. 를 출력해봅시다.
# ""로 강조하여 교수님 "수업"마쳐주세요. 를 출력해봅시다.
print("교수님 '수업'마쳐주세요.")
print('교수님 "수업"마쳐주세요.')

# print('교수님 ' 강조 '마쳐주세요')
