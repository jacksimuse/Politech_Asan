# 탈출문자 또는 이스케이프 문자 '\'를 사용해서 오류를 막아준다. 
# 원래 오류가 나는 문자를 허용해준다.
print('Python \'재미있\'습니다.')
print("Python \"재미있\"습니다.")


# ''로 감싸고 '' 강조하여 교수님 '수업'마쳐주세요. 를 출력해봅시다.
# ""로 감싸고 "" 강조하여 교수님 "수업"마쳐주세요. 를 출력해봅시다.
print('교수님 \'수업\'마쳐주세요.')
print("교수님 \"수업\"마쳐주세요.")


