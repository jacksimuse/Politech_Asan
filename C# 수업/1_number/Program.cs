using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _1_number
{
    class Program
    {
        static void Main(string[] args)
        {
            // byte 자료형 예제
            byte 바이트 = 1;
            Console.WriteLine("바이트 : " + 바이트);

            // byte 자료형을 이용하고 변수는 "숫자" 이름을 만들어주고, 숫자 2를 넣고 출력해봅시다.
            byte 숫자 = 2;
            Console.WriteLine(숫자);

            // short 자료형 예제
            short 에어컨온도 = 12;
            Console.WriteLine("에어컨온도 : " + 에어컨온도);

            // short 자료형을 이용하고 변수는 "숏" 이름을 만들어주고, 숫자 -1 넣어서 출력합시다.
            short 숏 = -1;
            Console.WriteLine(숏);

            // int 자료형 예제
            int 정수 = 0;
            Console.WriteLine("정수 : " + 정수);

            // int 자료형을 이용하고 변수는 "컴퓨터" 이름을 만들어주고, 숫자 12345 넣어서 출력합시다.
            int 컴퓨터 = 12345;
            Console.WriteLine(컴퓨터);

            // long 자료형 예제
            long 길다 = 123132564648948948;
            Console.WriteLine("길다 : " + 길다);

            // long 자료형을 이용하고 변수는 "거리" 이름을 만들어주고 숫자 -12458874416 넣어서 출력합시다.
            long 거리 = -12458874416;
            Console.WriteLine(거리);

        }
    }
}
