using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _1_string
{
    class Program
    {
        static void Main(string[] args)
        {
            // char 문자형 예제
            char 문 = 'ㄱ';
            Console.WriteLine(문);

            char a = 'a';
            Console.WriteLine(a);

            char 점 = '.';
            Console.WriteLine(점);

            // char 자료형을 이용해서 변수 "더하기"에 + 표시를 넣고 출력
            char 더하기 = '+';
            Console.WriteLine(더하기);

            // string 문자열형 예제
            string 여러글자 = "여러9+-/*/1230809213845234글자";
            Console.WriteLine(여러글자);

            // string 문자열을 이용해서 변수 "점심시간"에  밥 먹으러가자 를 넣고 출력
            string 점심시간 = "밥 먹으러가자";
            Console.WriteLine(점심시간);


        }
    }
}
